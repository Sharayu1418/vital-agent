"""Postgres migration guards.

Layer 1 (always runs): the schema renders correctly for both dialects,
placeholder translation is sound, and no module has slipped back into
SQLite-only SQL (INSERT OR REPLACE/IGNORE, lastrowid).

Layer 2 (opt-in): a full round-trip through storage + buddies + ingest
against a real Postgres. Set VITAL_TEST_DATABASE_URL to run, e.g.
    VITAL_TEST_DATABASE_URL=postgresql://localhost/vital_test pytest
Skipped otherwise so CI/local runs stay dependency-free.
"""
